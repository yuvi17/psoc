  <h4><i class="fa fa-history"></i> History</h4>
	<p>	
		In 1962 Roy studio a well known photo studio in Ranchi came to BIT Mesra with a proposal for a program for teaching photography to the students for some nominal fee. About 15 students enrolled in the program every sunday they used to go to Roy Studio in Ranchi & learn photography on a single mounted camera owned by the studio. These students formed a group called the Photography Club.
In 1963 the club grew in numbers to more than 50 members, by the end of the year the members realized the need for an executive body thus the first President of Photography Club was elected.
In 1964 Mr. P.S Shekhawat was elected the 2nd President of the club, it was during this time that the club was allotted a dark room in the main building. At that time Roy studio used to conduct a National Level Photography exhibition cum competition, Mr Shekhawat won this event twice & got monetary award of Rs. 5 .From a small dark room in campus, Photographic Society today 
has evolved as one of the finest clubs of BIT with more than 600+ members.
	</p>    
